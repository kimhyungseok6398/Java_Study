package exam05_Array;

public class Ex06 {
    public static void main(String[] args) {
        int [] nums = {10, 20, 30, 40};
        System.out.println("length : " + nums.length);
        // 공간에 갯수
        for (int i = 0; i < nums.length; i++){
            System.out.println(nums[i]);
        }
    }
}
